#Using Arthematic operators
a <- 5
7 -> b
print(a + b)      # "+" operator
print(a - b)      # "-" operator
print(a * b)      # "*" operator
print(a / b)      # "/" operator

#Relational Operators
 
print(a == b)

print(a > b)


print(a < b)


#Logical Operators
#   !, &, |    These are the operators

x <- TRUE
y <- FALSE

print("---------------")
print(!x)

print(x & y)

print(x | y)